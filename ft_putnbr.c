#include "libprintf.h"

void	ft_putlong(long long nb)
{
	if (nb < 0)
	{
		if (nb < -9223372036854775807)
			write(1, "-9223372036854775808", 20);
		else
		{
			write(1, "-", 1);
			nb = -nb;
		}
	}
	if (nb > 9)
		ft_putlong(nb / 10);
	ft_putchar((nb % 10) + '0');
}
